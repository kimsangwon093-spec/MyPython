# for 구문
# for i in range(1, 5): << 변수 범위
#   print(i) << 반복할 처리
# range() 명령 사용법
# range(반복 횟수)
# range(시작값, 종료값)
# range(시작값, 종료값, 증감값)
for i in range(10): # << range(반복 횟수)
    print(i)

for i in range(1, 5): # << range(시작값, 종료값)
    print(i)

for i in range(10, 0, -2): # << range(시작값, 종료값, 증감값)
    print(i)